def split_report(report,headings=["CLINICAL HISTORY:","TECHNIQUE:","COMMENTS:","IMPRESSION:"]):
    heading_locations = []
    pieces = {}
    for h in headings:
        heading_locations.append(report.find(h))
    for i in range(len(heading_locations)-1):
        pieces[headings[i]] = report[heading_locations[i]:heading_locations[i+1]]
    else:
        pieces[headings[-1]] = report[heading_locations[-1]:]
    return pieces
